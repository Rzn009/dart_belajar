void main() {
  var a = 10;
  var b = 5;
  var c = 12;
  var d = 10;

  //operator perbandingan  tanda lebih besar
  print(a > b);
  //operator perbandingan  tanda lebih kecil
  print(a < b);
  //operator perbandingan  tanda lebih besar sama dengan
  print(a >= b);
  //operator perbandingan  tanda kecil besar sama dengan
  print(a <= b);
  //tanda sama dengan
  print(a == b);
  //tamda negasi ! yaitu tanda kebalikan
  print(a != b);
  print(a > b);

  //tanda  && hasilnya false
  print((a > b) && (b < c));

  //||
  print((a < b) || (b > c));

}
