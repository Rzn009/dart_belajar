void main() {
  //opearator aritmatika
  var a = 1;
  var b = 5;

  //penjumlahan
  print(a + b);
  //pengurangan
  print(a - b);
  //perkalian
  print(a * b);
  //pembagian
  print(a / b.toInt());

  //modulus
  print(a % b);

  //perjumlahan increment
  print(a++);
  //pengurangan increment
  print(a--);
  //perjumlahan increment
  print(a += b);
  //perjumlahan increment
  print(a -= b);
}
